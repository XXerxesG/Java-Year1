
public class HollowSquare {
       public static void ln(int a){
    	   for(int i=0;i<a;i++){
    		   System.out.print("*");
    		   
    	   }
       }
       public static void sp(int b){
    	   for (int i=0;i<b;i++)
    		    
    		   if (i==0|| i==b-1){
    			   System.out.print("*");
    			  
    		   }
    	 
    		   else{
    			   System.out.print(" ");
    		   }
       }
}
